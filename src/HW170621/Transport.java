package HW170621;

public class Transport {
    int id;
    String name;
    int speed;
    int people;
    int cargo;
    String type;
    int price;

    public Transport(int id, String name, int speed, int people, int cargo, String type, int price) {
        this.id = id;
        this.name = name;
        this.speed = speed;
        this.people = people;
        this.cargo = cargo;
        this.type = type;
        this.price = price;
    }
}
